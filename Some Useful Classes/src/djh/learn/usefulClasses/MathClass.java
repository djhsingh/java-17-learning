import java.lang.Math.*;
public class MathClass {
    public static void main(String[] args) {
        
        System.out.println(Math.abs(56.5));
        System.out.println(Math.min(6, 3));
        System.out.println(Math.max(6, 7));
        System.out.println(Math.round(45.6));
        System.out.println(Math.sqrt(81));
        System.out.println(Math.pow(4, 3));
        System.out.println(Math.log(1000));
        System.out.println(Math.ceil(3.66));
        System.out.println(Math.ceil(3.26));
        System.out.println(Math.floor(3.66));
        System.out.println(Math.PI);
        System.out.println(Math.E);
        System.out.println(Math.ceil(Math.random()*10%6));
        System.out.println(Math.ceil(Math.random()*10%6));
    }
}
